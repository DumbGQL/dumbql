export interface TypeRef {
	base: string;
	nullable: boolean;
	list: boolean;
}

export interface CodegenScalars {
	[key: string]: string;
}

export interface CodegenTypesConfig {
	scalars?: CodegenScalars;
	enumsAsTypes?: boolean;
	maybeValue?: string;
	operationResultPrefix?: string;
	operationResultSuffix?: string;
}

const builtinTypes = new Set([
	'String', 'Int', 'Float', 'Boolean', 'ID',
	'__Schema', '__Type', '__TypeKind', '__Field', '__InputValue',
	'__EnumValue', '__Directive', '__DirectiveLocation',
]);

function resolveRef(typeRef: Record<string, unknown>): TypeRef {
	let current = typeRef;
	let nullable = true;
	let list = false;
	while (current.ofType) {
		if (current.kind === 'NON_NULL') nullable = false;
		if (current.kind === 'LIST') list = true;
		current = current.ofType as Record<string, unknown>;
	}
	return { base: current.name as string, nullable, list };
}

function toTsType(typeRef: Record<string, unknown>, scalars: CodegenScalars): string {
	if (!typeRef) return 'unknown';
	const { base, nullable, list } = resolveRef(typeRef);
	if (builtinTypes.has(base)) {
		const mapping: Record<string, string> = {
			String: 'string', Int: 'number', Float: 'number',
			Boolean: 'boolean', ID: 'string',
		};
		let ts = mapping[base] || scalars[base] || base;
		if (list) ts = `${ts}[]`;
		return nullable ? `${ts} | null | undefined` : ts;
	}
	let ts = scalars[base] || base;
	if (list) ts = `${ts}[]`;
	return nullable ? `${ts} | null | undefined` : ts;
}

function isBuiltin(name: string): boolean {
	return builtinTypes.has(name) || name.startsWith('__');
}

export interface SchemaType {
	name: string;
	kind: string;
	fields?: Array<Record<string, unknown>>;
	inputFields?: Array<Record<string, unknown>>;
	enumValues?: Array<Record<string, unknown>>;
	possibleTypes?: Array<Record<string, unknown>>;
	interfaces?: Array<Record<string, unknown>>;
}

export interface SchemaData {
	types: SchemaType[];
	queryType?: { name: string };
	mutationType?: { name: string };
	subscriptionType?: { name: string };
}

export function generateSchemaTypes(
	schema: SchemaData,
	scalars?: CodegenScalars,
	enumsAsTypes?: boolean,
): string {
	const scalarsMap = scalars || {};
	const lines: string[] = ['// Auto-generated by @dumbql/codegen. DO NOT EDIT.\n'];

	const allTypes = schema.types.filter((t) => !isBuiltin(t.name));

	// Enums
	for (const t of allTypes) {
		if (t.kind !== 'ENUM') continue;
		if (enumsAsTypes) {
			lines.push(`export type ${t.name} =`);
			for (const v of t.enumValues || []) lines.push(`  | '${v.name}'`);
			lines.push(';');
		} else {
			lines.push(`export const enum ${t.name} {`);
			for (const v of t.enumValues || []) lines.push(`  ${v.name} = '${v.name}',`);
			lines.push('}');
		}
		lines.push('');
	}

	// Input objects
	for (const t of allTypes) {
		if (t.kind !== 'INPUT_OBJECT') continue;
		lines.push(`export interface ${t.name} {`);
		for (const f of t.inputFields || []) {
			const ts = toTsType(f.type as Record<string, unknown>, scalarsMap);
			lines.push(`  ${f.name as string}: ${ts};`);
		}
		lines.push('}');
		lines.push('');
	}

	// Objects and Interfaces
	for (const t of allTypes) {
		if (t.kind !== 'OBJECT' && t.kind !== 'INTERFACE') continue;
		lines.push(`export interface ${t.name} {`);
		for (const f of t.fields || []) {
			const ts = toTsType(f.type as Record<string, unknown>, scalarsMap);
			lines.push(`  ${f.name as string}: ${ts};`);
		}
		lines.push('}');
		lines.push('');
	}

	// Unions
	for (const t of allTypes) {
		if (t.kind !== 'UNION') continue;
		lines.push(`export type ${t.name} =`);
		for (const pt of t.possibleTypes || []) lines.push(`  | ${pt.name as string}`);
		lines.push(';');
		lines.push('');
	}

	// Root types
	if (schema.queryType) {
		lines.push(`export type Query = ${schema.queryType.name};`);
	}
	if (schema.mutationType) {
		lines.push(`export type Mutation = ${schema.mutationType.name};`);
	}
	if (schema.subscriptionType) {
		lines.push(`export type Subscription = ${schema.subscriptionType.name};`);
	}

	return lines.join('\n');
}
